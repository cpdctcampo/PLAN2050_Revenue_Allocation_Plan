print("  - Includes 'Anticipated Opening Year' = last year with any funding")
print("  - CVTA funds used only for Chesterfield projects (sorted by B/C Rank)")
print("  - Front-loaded allocation: each project funded in earliest possible years, minimizing gaps")
print("  - Fully funded Chesterfield projects are removed from step 2")
print("  - Other revenue sources fund remaining projects")
print("  - Budget sweep ensures near-100% utilization of the 90% cap")
print("\n✓ df3_budget_comparison_cvta.csv")
print("  - Shows annual original, 90% cap, programmed, and utilization for all sources (including cvta)")











